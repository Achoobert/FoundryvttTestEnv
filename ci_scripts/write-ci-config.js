/**
 * Write fvtt.config.js + .env in consumer workspace from action inputs / optional example file.
 */
import * as fs from 'node:fs'
import * as path from 'node:path'
import { pathToFileURL } from 'node:url'
import { MODULE_ROOT, readModuleJsonId } from './fvtt-paths.js'

const DEFAULT_QUENCH_MANIFEST =
  'https://github.com/Ethaks/FVTT-Quench/releases/download/v0.10.0/module.json'

const DEFAULT_TEST_SYSTEM_MANIFEST =
  'https://github.com/deltagreen-foundryvtt/delta-green-foundry-vtt-system/releases/download/v1.7.0/system.json'

function parseModuleIdList(raw) {
  if (!raw || !String(raw).trim()) return []
  return String(raw)
    .split(',')
    .map((s) => s.trim())
    .filter(Boolean)
}

function deriveTestModuleIds(quenchTestsPath) {
  const ids = ['quench']
  const mainId = readModuleJsonId(path.join(MODULE_ROOT, 'module.json'))
  if (mainId) ids.push(mainId)

  if (quenchTestsPath) {
    const rel = quenchTestsPath.replace(/^\.\//, '')
    const testsId = readModuleJsonId(
      path.join(MODULE_ROOT, rel, 'module', 'module.json')
    )
    if (testsId) ids.push(testsId)
  }

  return [...new Set(ids)]
}

const userDataPath = path.resolve(
  process.env.FOUNDRY_USERDATA_HOST || path.join(MODULE_ROOT, 'foundrydata')
)
fs.mkdirSync(userDataPath, { recursive: true })

const fvttConfigPath = process.env.FOUNDRY_FVTT_CONFIG_PATH || 'fvtt.config.example.js'
const exampleFull = path.join(MODULE_ROOT, fvttConfigPath)

let base = {
  userDataPath,
  baseURL: 'http://localhost:30000',
  testWorldName: process.env.FOUNDRY_WORLD || 'foundry-test-world',
  testModuleIds: [],
  dockerContainerName:
    process.env.FOUNDRY_CONTAINER_NAME ||
    `foundry-${process.env.FOUNDRY_WORLD || 'test'}`,
  quenchManifestUrl:
    process.env.FOUNDRY_QUENCH_MANIFEST_URL || DEFAULT_QUENCH_MANIFEST,
  testSystemManifestUrl:
    process.env.FOUNDRY_TEST_SYSTEM_MANIFEST_URL || DEFAULT_TEST_SYSTEM_MANIFEST
}

if (fs.existsSync(exampleFull)) {
  const exampleModule = await import(pathToFileURL(exampleFull).href)
  base = { ...base, ...exampleModule.default, userDataPath }
}

const explicitIds = parseModuleIdList(process.env.FOUNDRY_TEST_MODULE_IDS)
const quenchPath = process.env.FOUNDRY_QUENCH_TESTS_PATH || ''
base.testModuleIds =
  explicitIds.length > 0 ? explicitIds : deriveTestModuleIds(quenchPath)

if (!base.testWorldName) {
  base.testWorldName = process.env.FOUNDRY_WORLD || 'foundry-test-world'
}

const containerName =
  process.env.FOUNDRY_CONTAINER_NAME ||
  base.dockerContainerName ||
  `foundry-${base.testWorldName}`

const fvttOut = `/** Generated by FoundryvttTestEnv write-ci-config.js */\nconst developmentOptions = ${JSON.stringify(base, null, 2)}\n\nexport default developmentOptions\n`
fs.writeFileSync(path.join(MODULE_ROOT, 'fvtt.config.js'), fvttOut, 'utf8')
console.log('Wrote fvtt.config.js userDataPath:', userDataPath)

const world = process.env.FOUNDRY_WORLD ?? base.testWorldName
const foundryVersion = process.env.FOUNDRY_VERSION || process.env.FOUNDRY_CORE_VERSION || '14.364'

const envLines = [
  '# Generated by FoundryvttTestEnv write-ci-config.js',
  `FOUNDRY_USERDATA_HOST=${userDataPath}`,
  `FOUNDRY_WORLD=${world}`,
  `FOUNDRY_CONTAINER_NAME=${containerName}`,
  `FOUNDRY_VERSION=${foundryVersion}`,
  ''
]
fs.writeFileSync(path.join(MODULE_ROOT, '.env'), envLines.join('\n'), 'utf8')
console.log('Wrote .env')

const dockerRoot = path.join(MODULE_ROOT, '.foundry-docker')
fs.mkdirSync(path.join(dockerRoot, 'secret'), { recursive: true })
